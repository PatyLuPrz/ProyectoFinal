    private void JButtonBuscarProd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonBuscarProd1ActionPerformed

        try {
            DefaultTableModel modelo = new DefaultTableModel();
            jTableInventarioVenta.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.getConexion();
            String buscarProducto = this.jTextFieldBuscarProd1.getText();
            String tipobusq = null;

            int tipo = jComboBoxTipoBusqProd1.getSelectedIndex();

            switch (tipo) {//Se encarga de evaluar el combobox
                case 1:
                    tipobusq = "CODIGO_P";
                    break;
                case 2:
                    tipobusq = "NOMBRE_P";
                    break;
                case 3:
                    tipobusq = "CATEGORIA_P";
                    break;
                case 4:
                    tipobusq = "MARCA_P";
                    break;
                case 5:
                    tipobusq = "CANTIDAD_P";
                    break;
                case 6:
                    tipobusq = "PRECIO_VENTA_P";
                    break;
            }

            String sql = "SELECT CODIGO_P, NOMBRE_P, CATEGORIA_P, MARCA_P, CANTIDAD_P, PRECIO_VENTA_P FROM productos WHERE " + tipobusq + " LIKE '%" + buscarProducto + "%'";
            System.out.println(rs);

            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Codigo");
            modelo.addColumn("Nombre");
            modelo.addColumn("Categoria");
            modelo.addColumn("Marca");
            modelo.addColumn("Cantidad");
            modelo.addColumn("Precio Venta");

            while (rs.next()) {

                Object[] filas = new Object[cantidadColumnas];

                for (int i = 0; i < cantidadColumnas; i++) {

                    filas[i] = rs.getObject(i + 1);
                }

                modelo.addRow(filas);

            }
        } catch (SQLException ex) {
            System.out.println("Error" + ex);
        }

    }//GEN-LAST:event_JButtonBuscarProd1ActionPerformed

