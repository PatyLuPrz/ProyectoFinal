package modelo;

/**
 *
 * @author beto
 */
public class usuarios {
    private String username;
    private String email_user;
    private String contrasena_user;
    private String nombre_user;
    private String ap_user;
    private String am_user;
    private String tipo_user;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail_user() {
        return email_user;
    }

    public void setEmail_user(String email_user) {
        this.email_user = email_user;
    }

    public String getContrasena_user() {
        return contrasena_user;
    }

    public void setContrasena_user(String contrasena_user) {
        this.contrasena_user = contrasena_user;
    }

    public String getNombre_user() {
        return nombre_user;
    }

    public void setNombre_user(String nombre_user) {
        this.nombre_user = nombre_user;
    }

    public String getAp_user() {
        return ap_user;
    }

    public void setAp_user(String ap_user) {
        this.ap_user = ap_user;
    }

    public String getAm_user() {
        return am_user;
    }

    public void setAm_user(String am_user) {
        this.am_user = am_user;
    }

    public String getTipo_user() {
        return tipo_user;
    }

    public void setTipo_user(String tipo_user) {
        this.tipo_user = tipo_user;
    }
    
}
